# One representative query per classifier.py::QueryCategory.
_QUERY_CATEGORY_SAMPLES: tuple[tuple[str, str], ...] = (
    ("why", "Why is Acme at risk?"),
    ("show_me", "Show me the open commitments this week."),
    ("draft", "Draft a brief for Monica."),
    ("what_if", "What if we extend the Acme renewal 30 days?"),
    ("summary", "What happened yesterday?"),
    ("arbitrary", "Where is the company silent where it shouldn't be?"),
)


pytestmark_query_coverage = pytest.mark.skipif(
    os.environ.get("SECTION7_QUERY_COVERAGE") != "1",
    reason="set SECTION7_QUERY_COVERAGE=1 to run (requires live LLM + scenario replayed)",
)


@pytest.fixture
async def gateway_app():
    """Build the gateway app with lifespan so all Week-4+ routers mount."""
    from services.gateway.main import build_app

    app = build_app()
    async with app.router.lifespan_context(app):
        yield app


@pytest.mark.asyncio
@pytestmark_query_coverage
@pytest.mark.parametrize("category,query", _QUERY_CATEGORY_SAMPLES)
async def test_section7_test4_query_coverage(
    gateway_app, category: str, query: str
) -> None:
    """§7 Test 4: each of the 6 classifier categories produces a
    non-empty, well-formed `response_html`."""
    import httpx

    transport = httpx.ASGITransport(app=gateway_app)
    async with httpx.AsyncClient(
        transport=transport, base_url="http://gateway", timeout=120
    ) as client:
        r = await client.post(
            "/view/ceo/ask",
            json={"query": query},
            headers={"x-tenant-id": str(DOGFOOD_TENANT)},
        )
    assert r.status_code == 200, r.text
    body = r.json()
    for key in (
        "turn_id", "query_echo", "response_html",
        "verbs", "computed_at", "latency_ms",
    ):
        assert key in body, f"[{category}] missing {key!r}"
    assert isinstance(body["response_html"], str)
    html = body["response_html"].strip()
    assert html, f"[{category}] empty response_html"
    assert not html.startswith("{"), (
        f"[{category}] JSON-wrap leak in response_html"
    )
    # Voice-rules-compliant HTML never contains exclamation marks.
    assert "!" not in html, f"[{category}] response_html contains !"


# ---------------------------------------------------------------------
# §7 Test 5 — staleness behavior.
#
# Forces a cache row's `cached_at` backward 1h, asserts /view/ceo/home
# reports `staleness_seconds > 3500`. Runs when DATABASE_URL is set.
# ---------------------------------------------------------------------


@pytest.mark.asyncio
@pytest.mark.integration
async def test_section7_test5_staleness_threshold(monkeypatch) -> None:
    if not os.environ.get("DATABASE_URL"):
        pytest.skip("DATABASE_URL unset — integration only")
    import asyncpg
    from services.gateway.main import build_app

    # Ensure the scheduler doesn't race us by overwriting the cached_at
    # we're about to backdate.
    monkeypatch.setenv("GATEWAY_START_GRT_SCHEDULER", "0")
    # Ensure the dogfood tenant id is set so the default tenant is
    # registered.
    monkeypatch.setenv(
        "DEFAULT_TENANT_ID", str(DOGFOOD_TENANT)
    )

    app = build_app()
    async with app.router.lifespan_context(app):
        dsn = os.environ["DATABASE_URL"]
        # Seed a minimal cache row if missing, then backdate it.
        async with asyncpg.create_pool(dsn, min_size=1, max_size=2) as pool:
            async with pool.acquire() as conn:
                row = await conn.fetchrow(
                    """
                    SELECT cache_key FROM view_ceo_cache
                    WHERE tenant_id = $1 AND cache_key = 'greeting'
                    """,
                    DOGFOOD_TENANT,
                )
                if row is None:
                    # Seed a tiny placeholder greeting so the endpoint
                    # has something to read.
                    await conn.execute(
                        """
                        INSERT INTO view_ceo_cache
                            (tenant_id, cache_key, cached_content, cached_at)
                        VALUES ($1, 'greeting', $2, now())
                        ON CONFLICT (tenant_id, cache_key) DO UPDATE
                            SET cached_content = EXCLUDED.cached_content,
                                cached_at = now()
                        """,
                        DOGFOOD_TENANT,
                        json.dumps({
                            "meta": {
                                "date_iso": "2026-04-22",
                                "recomputed_at": "2026-04-22T00:00:00Z",
                                "signals_watched_count": 0,
                            },
                            "body_html": "Good morning.",
                            "cached_at": "2026-04-22T00:00:00Z",
                        }),
                    )
                # Backdate.
                await conn.execute(
                    """
                    UPDATE view_ceo_cache
                    SET cached_at = now() - interval '1 hour'
                    WHERE tenant_id = $1 AND cache_key = 'greeting'
                    """,
                    DOGFOOD_TENANT,
                )

        import httpx

        transport = httpx.ASGITransport(app=app)
        async with httpx.AsyncClient(
            transport=transport, base_url="http://gateway"
        ) as client:
            r = await client.get("/view/ceo/home")
        assert r.status_code == 200, r.text
        body = r.json()
        staleness = body["greeting"]["staleness_seconds"]
        assert staleness > 3500, (
            f"expected staleness_seconds > 3500 after 1h backdate, "
            f"got {staleness!r}"
        )
